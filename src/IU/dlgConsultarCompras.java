    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int rpta = Mensajes.confirmar("Desea Anular esta Compra");
            if (rpta == 0) {
                controlCompra.anularCompra();
                CabecerasTablas.limpiarTablas(jTable1);
                CabecerasTablas.limpiarTablas(jTable2);
                cabe.consCompras(jTable1);
                cabe.consDetalleCompras(jTable2);
                controlCompra.listarCompras(jTable1);
            }

        } catch (Exception e) {
            Mensajes.informacion("Seleccione la fila a eliminar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

